const mongoose = require("mongoose");

const SignupSchema = mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

const SignupModel = mongoose.model("Signup", SignupSchema);

module.exports = SignupModel;
